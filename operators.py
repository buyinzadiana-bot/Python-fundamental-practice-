# Arithmetic (math)
# +, -, *, /, %, ** (exponentiation), // (floor division)

a = 10
b = 5

print (a + b) # 15
print (a - b) # 5
print (a * b) # 50
print (a / b) # 2.0
print (a % b) # 0
print (a ** b) # 100000
print (a // b) # 2

# comaparison
print (a > b) # True
print (a < b) # False
print (a == b) # False
print (a != b) # True

# logical combining question

print (a > 5 and b < 10) # True
print (a > 5 or b > 10) # True  
print (not (a > 5)) # False 